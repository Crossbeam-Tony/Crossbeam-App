class MarketplaceItem {
  final String id;
  final String title;
  final String description;
  final double price;
  final String sellerId;
  final String imageUrl;

  MarketplaceItem({
    required this.id,
    required this.title,
    required this.description,
    required this.price,
    required this.sellerId,
    required this.imageUrl,
  });
}
